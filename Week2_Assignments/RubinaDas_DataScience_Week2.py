"""**Task 4: File Handling**
1. Create a text file named student_data.txt.
2. Write the following information into the file (name, course, and marks of at least 5 students).
3. Read the file and display only those students whose marks are above 75.
"""

students = [
    "Rubina Das | AI | 80.8",
    "Soumya Verma | Java | 90",
    "Manas Mondal | Maths | 75",
    "Rahul | English | 50",
    "Veer Singh Rathore | Computer | 85.6"
]

# A file named student_data.txt is created which contains details of students (name, course, marks)

with open('student_data.txt', 'w') as file:
  file.write('\n'.join(students))

print("All students details are printed whose marks is greater than 75 . . .")

# The file is opened in read mode
with open('student_data.txt', 'r') as file:
  for line in file:
    name, course, marks = line.strip().split('|')   # Each student details are splitted into list values

    # Student details are printed where marks is greater than 75
    if float(marks.strip()) > 75.0:
      print(f'Name: {name.strip()}, Course: {course.strip()}, Marks: {float(marks.strip())}')
