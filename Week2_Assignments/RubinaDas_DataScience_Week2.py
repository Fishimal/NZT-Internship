# A list is created with 15 numbers and the list contains integer and float values
numbers = [77, 78.5, 86, 85.3, 99, 100, 10, -12.5, -35.7, -45, 509, -239, -687.9, -700, 888]

length_of_list_numbers = len(numbers)             # len() function returns the length of the list
maximum_element_of_list_numbers = max(numbers)    # max() function returns the maximum value of the list
minimum_element_of_list_numbers = min(numbers)    # min() function returns the maximum value of the list

# The results are printed using f-strings
print(f'The original list: {numbers}')
print(f'Length of the list = {length_of_list_numbers}')
print(f'Maximum value of the list = {maximum_element_of_list_numbers}')
print(f'Minimum value of the list = {minimum_element_of_list_numbers}')

numbers.append(256)   # a new number is added to the list
numbers.remove(100)   # an existing number is deleted from the list

# The list is printed after modification
print(f'The list after adding an element and removing an element: {numbers}')

ascending_order_numbers = sorted(numbers)                   # the list is sorted in ascending order
descending_order_numbers = ascending_order_numbers[::-1]    # the list is sorted in descending order

# The sorted lists are printed
print(f'List in ascending order: {ascending_order_numbers}')
print(f'List in descending order: {descending_order_numbers}')

# numpy module is imported and np is just another reference or alias which will be used further to access the functions and attributes of the module
import numpy as np

arr = np.array(numbers)                   # converting the list into a numpy array
mean = np.mean(numbers)                   # calculates the mean or average of the numpy array
median = np.median(numbers)               # calculates the median of the numpy array
standard_deviation = np.std(numbers)      # calculates the standard deviation of the numpy array

# All the results are printed using f-strings
print(f'The numpy arra: {arr}')
print(f'Validating the type of numpy array: {type(arr)}')
print(f'The mean of the numpy array = {mean}')
print(f'The median of the numpy array = {median}')
print(f'The standard deviation of the numpy array = {standard_deviation}')

